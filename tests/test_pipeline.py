class TestPDFIngestion:
    """Test the PDF ingestion layer."""

    def test_pdf_loads_all_pages(self, synthetic_pdf: Path):
        """The loader should extract all 3 pages from the synthetic PDF."""
        from ingestion.pdf_loader import load_pdf

        pages = load_pdf(str(synthetic_pdf))

        assert len(pages) == 3, f"Expected 3 pages, got {len(pages)}"

    def test_pdf_has_text(self, synthetic_pdf: Path):
        """Every page should contain non-empty text."""
        from ingestion.pdf_loader import load_pdf

        pages = load_pdf(str(synthetic_pdf))

        for page in pages:
            assert page["text"].strip(), f"Page {page['page']} has no text"

    def test_pdf_tables_extracted(self, synthetic_pdf: Path):
        """At least some pages should have extracted tables."""
        from ingestion.pdf_loader import load_pdf

        pages = load_pdf(str(synthetic_pdf))
        total_tables = sum(len(p.get("tables", [])) for p in pages)

        assert total_tables >= 1, (
            f"Expected at least 1 table across 3 pages, got {total_tables}"
        )

    def test_pdf_metadata(self, synthetic_pdf: Path):
        """Each page dict should carry source and title metadata."""
        from ingestion.pdf_loader import load_pdf

        pages = load_pdf(str(synthetic_pdf))

        for page in pages:
            assert "source" in page, "Missing 'source' key"
            assert "page" in page, "Missing 'page' key"
            assert "title" in page, "Missing 'title' key"
            assert page["source"].endswith(".pdf")

    def test_pdf_file_not_found(self):
        """Should raise FileNotFoundError for a missing file."""
        from ingestion.pdf_loader import load_pdf

        with pytest.raises(FileNotFoundError):
            load_pdf("nonexistent_report.pdf")


