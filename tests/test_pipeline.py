@pytest.fixture
def synthetic_pdf(tmp_path) -> Path:
    """Generate a 3-page synthetic financial PDF using reportlab.

    Numbers are chosen to produce:
    - debt_to_equity ≈ 2.8   (total_debt=1,400M / total_equity=500M)
    - current_ratio  ≈ 0.9   (current_assets=450M / current_liabilities=500M)
    - declining net income:   Q1=120M → Q2=85M → Q3=42M

    Returns
    -------
    Path
        Path to the generated PDF file.
    """
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import letter
    from reportlab.lib.units import inch
    from reportlab.platypus import (
        SimpleDocTemplate,
        Table,
        TableStyle,
        Paragraph,
        Spacer,
        PageBreak,
    )
    from reportlab.lib.styles import getSampleStyleSheet

    pdf_path = tmp_path / "acme_corp_annual_2024.pdf"
    doc = SimpleDocTemplate(str(pdf_path), pagesize=letter)
    styles = getSampleStyleSheet()
    elements = []

    # ── Shared table style ──────────────────────────────────────────
    table_style = TableStyle(
        [
            ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#334155")),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, 0), 10),
            ("BOTTOMPADDING", (0, 0), (-1, 0), 8),
            ("BACKGROUND", (0, 1), (-1, -1), colors.HexColor("#f8fafc")),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
            ("FONTSIZE", (0, 1), (-1, -1), 9),
            ("TOPPADDING", (0, 1), (-1, -1), 4),
            ("BOTTOMPADDING", (0, 1), (-1, -1), 4),
        ]
    )

    # ── PAGE 1: Balance Sheet ───────────────────────────────────────
    elements.append(Paragraph("ACME Corporation — Annual Report 2024", styles["Title"]))
    elements.append(Spacer(1, 0.3 * inch))
    elements.append(Paragraph("Consolidated Balance Sheet (USD millions)", styles["Heading2"]))
    elements.append(Spacer(1, 0.15 * inch))

    balance_data = [
        ["Item", "2024", "2023"],
        ["Total Assets", "2,100", "2,050"],
        ["Current Assets", "450", "520"],
        ["Cash and Cash Equivalents", "120", "180"],
        ["Short-term Investments", "80", "90"],
        ["Accounts Receivable", "150", "160"],
        ["Inventory", "100", "90"],
        ["Total Liabilities", "1,600", "1,500"],
        ["Current Liabilities", "500", "480"],
        ["Total Debt", "1,400", "1,300"],
        ["Total Equity", "500", "550"],
        ["Shareholder Equity", "500", "550"],
    ]

    tbl = Table(balance_data, colWidths=[2.5 * inch, 1.5 * inch, 1.5 * inch])
    tbl.setStyle(table_style)
    elements.append(tbl)
    elements.append(PageBreak())

    # ── PAGE 2: P&L Statement ───────────────────────────────────────
    elements.append(
        Paragraph("Income Statement — Quarterly Breakdown (USD millions)", styles["Heading2"])
    )
    elements.append(Spacer(1, 0.15 * inch))

    pnl_data = [
        ["Item", "Q1 2024", "Q2 2024", "Q3 2024"],
        ["Revenue", "800", "780", "750"],
        ["Cost of Goods Sold", "520", "530", "545"],
        ["Gross Income", "280", "250", "205"],
        ["Operating Expenses", "120", "125", "128"],
        ["Operating Income", "160", "125", "77"],
        ["Interest Expense", "25", "27", "30"],
        ["Net Income", "120", "85", "42"],
    ]

    tbl2 = Table(
        pnl_data,
        colWidths=[2.0 * inch, 1.3 * inch, 1.3 * inch, 1.3 * inch],
    )
    tbl2.setStyle(table_style)
    elements.append(tbl2)

    elements.append(Spacer(1, 0.3 * inch))
    elements.append(
        Paragraph(
            "Note: Net income declined from 120M in Q1 to 42M in Q3 due to "
            "rising cost of goods sold and increased interest expense. "
            "Profit margin fell from 15.0% to 5.6%.",
            styles["Normal"],
        )
    )
    elements.append(PageBreak())

    # ── PAGE 3: Cash Flow Statement ─────────────────────────────────
    elements.append(
        Paragraph("Cash Flow Statement — Full Year (USD millions)", styles["Heading2"])
    )
    elements.append(Spacer(1, 0.15 * inch))

    cashflow_data = [
        ["Item", "2024", "2023"],
        ["Cash from Operations", "210", "290"],
        ["Capital Expenditures", "-95", "-80"],
        ["Free Cash Flow", "115", "210"],
        ["Dividends Paid", "-60", "-55"],
        ["Debt Repayment", "-50", "-70"],
        ["Net Change in Cash", "-60", "5"],
    ]

    tbl3 = Table(cashflow_data, colWidths=[2.5 * inch, 1.5 * inch, 1.5 * inch])
    tbl3.setStyle(table_style)
    elements.append(tbl3)

    elements.append(Spacer(1, 0.3 * inch))
    elements.append(
        Paragraph(
            "Cash from operations declined 28% year-over-year. "
            "Free cash flow dropped 45% from 210M to 115M. "
            "The company drew down cash reserves by 60M to fund operations.",
            styles["Normal"],
        )
    )

    doc.build(elements)
    return pdf_path


