class TestRAGPipeline:
    """Test the chunking → embedding → retrieval pipeline."""

    def test_chunker_produces_documents(self, synthetic_pdf: Path):
        """Chunking the ingested pages should produce LangChain Documents."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        assert len(chunks) > 0, "Chunker produced no chunks"

        # Each chunk should be a LangChain Document.
        from langchain.schema import Document

        for c in chunks:
            assert isinstance(c, Document)
            assert c.page_content.strip()
            assert "chunk_index" in c.metadata
            assert "char_count" in c.metadata

    def test_faiss_store_persisted(self, synthetic_pdf: Path, tmp_vector_store):
        """Embedding should create a FAISS index on disk."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        store = embedder.embed_and_store(chunks, tmp_vector_store)

        assert store is not None
        assert store.index.ntotal > 0, "FAISS index has no vectors"

        # Files should exist on disk.
        store_dir = Path(tmp_vector_store)
        assert (store_dir / "index.faiss").exists()
        assert (store_dir / "index.pkl").exists()

    def test_retrieval_returns_results(self, synthetic_pdf: Path, tmp_vector_store):
        """Retrieving for a financial query should return relevant chunks."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder
        from rag.retriever import FinancialRetriever

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        store = embedder.embed_and_store(chunks, tmp_vector_store)

        retriever = FinancialRetriever(store)
        results = retriever.retrieve("debt equity ratio", k=3)

        assert len(results) > 0, "Retriever returned no results"
        assert len(results) <= 3

    def test_scored_retrieval(self, synthetic_pdf: Path, tmp_vector_store):
        """Scored retrieval should return tuples of (Document, float)."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder
        from rag.retriever import FinancialRetriever

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        store = embedder.embed_and_store(chunks, tmp_vector_store)

        retriever = FinancialRetriever(store)
        results = retriever.retrieve_with_scores("cash flow", k=3)

        assert len(results) > 0
        for doc, score in results:
            assert isinstance(score, float)
            assert score >= 0.0

    def test_hybrid_retrieval(self, synthetic_pdf: Path, tmp_vector_store):
        """Hybrid retrieval should boost results containing keywords."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder
        from rag.retriever import FinancialRetriever

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        store = embedder.embed_and_store(chunks, tmp_vector_store)

        retriever = FinancialRetriever(store)
        results = retriever.hybrid_retrieve(
            query="financial risk assessment",
            keywords=["debt", "equity", "liabilities"],
            k=3,
        )

        assert len(results) > 0


