class TestFinancialRatios:
    """Unit tests for all 5 ratio methods + interpret_ratio."""

    def setup_method(self):
        from utils.financial_ratios import FinancialRatioEngine

        self.engine = FinancialRatioEngine()

    # ── debt_to_equity ──────────────────────────────────────────────

    def test_debt_to_equity_normal(self):
        result = self.engine.debt_to_equity(1_400, 500)
        assert result == 2.8

    def test_debt_to_equity_zero_equity(self):
        result = self.engine.debt_to_equity(1_000, 0)
        assert result is None

    def test_debt_to_equity_negative_debt(self):
        result = self.engine.debt_to_equity(-200, 500)
        assert result == -0.4

    def test_debt_to_equity_zero_debt(self):
        result = self.engine.debt_to_equity(0, 500)
        assert result == 0.0

    # ── current_ratio ───────────────────────────────────────────────

    def test_current_ratio_normal(self):
        result = self.engine.current_ratio(450, 500)
        assert result == 0.9

    def test_current_ratio_zero_liabilities(self):
        result = self.engine.current_ratio(450, 0)
        assert result is None

    def test_current_ratio_healthy(self):
        result = self.engine.current_ratio(3_000, 1_000)
        assert result == 3.0

    # ── profit_margin ───────────────────────────────────────────────

    def test_profit_margin_normal(self):
        result = self.engine.profit_margin(120, 800)
        assert result == 0.15

    def test_profit_margin_zero_revenue(self):
        result = self.engine.profit_margin(120, 0)
        assert result is None

    def test_profit_margin_loss(self):
        result = self.engine.profit_margin(-50, 800)
        assert result == -0.0625

    # ── debt_to_income ──────────────────────────────────────────────

    def test_debt_to_income_normal(self):
        result = self.engine.debt_to_income(360, 1_000)
        assert result == 0.36

    def test_debt_to_income_zero_income(self):
        result = self.engine.debt_to_income(500, 0)
        assert result is None

    def test_debt_to_income_high_risk(self):
        result = self.engine.debt_to_income(500, 1_000)
        assert result == 0.5  # > 0.43 threshold

    # ── quick_ratio ─────────────────────────────────────────────────

    def test_quick_ratio_normal(self):
        # (120 + 80 + 150) / 500 = 350 / 500 = 0.7
        result = self.engine.quick_ratio(120, 80, 150, 500)
        assert result == 0.7

    def test_quick_ratio_zero_liabilities(self):
        result = self.engine.quick_ratio(100, 50, 50, 0)
        assert result is None

    def test_quick_ratio_all_zero_numerator(self):
        result = self.engine.quick_ratio(0, 0, 0, 500)
        assert result == 0.0

    # ── interpret_ratio ─────────────────────────────────────────────

    def test_interpret_debt_to_equity_critical(self):
        result = self.engine.interpret_ratio("debt_to_equity", 2.8)
        assert result["status"] == "critical"
        assert result["value"] == 2.8

    def test_interpret_current_ratio_critical(self):
        result = self.engine.interpret_ratio("current_ratio", 0.9)
        assert result["status"] == "critical"

    def test_interpret_current_ratio_safe(self):
        result = self.engine.interpret_ratio("current_ratio", 2.0)
        assert result["status"] == "safe"

    def test_interpret_profit_margin_warning(self):
        result = self.engine.interpret_ratio("profit_margin", 0.07)
        assert result["status"] == "warning"

    def test_interpret_quick_ratio_warning(self):
        result = self.engine.interpret_ratio("quick_ratio", 0.8)
        assert result["status"] == "warning"

    def test_interpret_none_value(self):
        result = self.engine.interpret_ratio("debt_to_equity", None)
        assert result["status"] == "error"

    def test_interpret_unknown_ratio(self):
        with pytest.raises(ValueError, match="Unknown ratio"):
            self.engine.interpret_ratio("nonexistent_ratio", 1.0)

    def test_interpret_all_thresholds_have_keys(self):
        """Every interpretation should have value, status, and threshold."""
        test_cases = [
            ("debt_to_equity", 1.5),
            ("current_ratio", 1.2),
            ("profit_margin", 0.12),
            ("debt_to_income", 0.30),
            ("quick_ratio", 1.1),
        ]
        for name, val in test_cases:
            result = self.engine.interpret_ratio(name, val)
            assert "value" in result
            assert "status" in result
            assert "threshold" in result
            assert result["status"] in ("safe", "warning", "critical")


