class TestFullPipeline:
    """End-to-end integration test using the synthetic PDF and mocked LLM."""

    def test_full_analysis(self, synthetic_pdf: Path, tmp_vector_store):
        """run_analysis should produce a complete report with all expected fields."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder
        from graph.workflow import run_analysis

        # ── Ingest & embed ──────────────────────────────────────────
        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        embedder.embed_and_store(chunks, tmp_vector_store)

        # ── Override VECTOR_DB_PATH for the workflow ────────────────
        os.environ["VECTOR_DB_PATH"] = tmp_vector_store

        # ── Run the full pipeline ───────────────────────────────────
        from langchain.schema import Document as LCDoc

        docs = [
            LCDoc(page_content=p["text"], metadata={"source": p["source"]})
            for p in pages
        ]

        report = run_analysis(
            query="Analyze credit risk for ACME Corporation based on their 2024 annual report",
            documents=docs,
        )

        # ── Assertions ──────────────────────────────────────────────
        assert report is not None, "run_analysis returned None"
        assert isinstance(report, dict), "report should be a dict"

        # Risk level
        assert "risk_level" in report
        assert report["risk_level"] in ("LOW", "MEDIUM", "HIGH", "CRITICAL"), (
            f"Unexpected risk_level: {report['risk_level']}"
        )

        # Key metrics
        assert "key_metrics" in report
        assert isinstance(report["key_metrics"], list)

        # Sources
        assert "sources" in report
        assert isinstance(report["sources"], list)

        # Agent trace — all 6 agents should have executed
        assert "agent_trace" in report
        trace = report["agent_trace"]
        expected_agents = {"planner", "retriever", "analyst", "evaluator", "verifier", "reporter"}
        actual_agents = set(trace)
        assert expected_agents.issubset(actual_agents), (
            f"Missing agents in trace: {expected_agents - actual_agents}. "
            f"Trace: {trace}"
        )

        # Report structure completeness
        assert "summary" in report
        assert "risk_badge" in report
        assert "justification" in report
        assert "recommendations" in report
        assert "verification_status" in report
        assert "verification_notes" in report
        assert "anomalies" in report

    def test_report_has_badge(self, synthetic_pdf: Path, tmp_vector_store):
        """The risk badge should contain an emoji and the level."""
        from ingestion.pdf_loader import load_pdf
        from rag.chunker import DocumentChunker
        from rag.embedder import DocumentEmbedder
        from graph.workflow import run_analysis

        pages = load_pdf(str(synthetic_pdf))
        chunker = DocumentChunker()
        chunks = chunker.chunk_documents(pages)

        embedder = DocumentEmbedder()
        embedder.embed_and_store(chunks, tmp_vector_store)
        os.environ["VECTOR_DB_PATH"] = tmp_vector_store

        report = run_analysis(query="Assess ACME Corp credit risk")

        badge = report.get("risk_badge", "")
        level = report.get("risk_level", "")
        assert level in badge, (
            f"Badge '{badge}' should contain level '{level}'"
        )
